    private void btnLoguinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoguinActionPerformed
        // TODO add your handling code here:
        if (TelaCadastro.listaUsuario.isEmpty()) {
            jLabelAvisos.setVisible(true);
            jLabelAvisos.setText("USUARIO N√ÉO CADASTRADO!!");

        } else {

            for (Usuario listaUsuario : TelaCadastro.listaUsuario) {

                if (txtLogin.getText().equals(listaUsuario.getLogin())) {

                    if (txtSenha.getText().equals(listaUsuario.getSenha())) {
                        jLabelAvisos.setVisible(false);
                        telaSistema.setVisible(true);
                       

                    } else {
                        jLabelAvisos.setVisible(true);
                        jLabelAvisos.setText("SENHA INVALIDA!!");

                    }

                } else {
                    jLabelAvisos.setVisible(true);
                    jLabelAvisos.setText("LOGUIN INVALIDO!!");

                }
            }

        }
        txtLogin.setText(null);
        txtSenha.setText(null);
    }//GEN-LAST:event_btnLoguinActionPerformed

