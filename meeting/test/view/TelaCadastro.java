    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        if("USUARIO COMUM".equals(jcbAcessoSys.getSelectedItem())){
            user = new Usuario();
   
        }else if("COORDENADOR".equals(jcbAcessoSys.getSelectedItem())){
            user = new Coordenador();
            
        }else if("GESTOR DE RECURSO".equals(jcbAcessoSys.getSelectedItem())){
            user = new GestorDeRecursos();
            
        }else{
            throw new IllegalAccessError("ACESSO DESCONHECIDO!!");
        }
        
        user.setNome(txtNome.getText());
        
        user.setLogin(txtLoguin.getText());
        
        user.setSenha(txtSenha.getText());

        Random idUser = new  Random();
        user.setIdUser(String.valueOf(idUser.hashCode()));

        listaUsuario.add(user);
   
        txtLoguin.setText(null);
        txtNome.setText(null);
        txtSenha.setText(null);
        
        // depois ver uma maneira de verificar se Ã¡h dados repetidos
    }//GEN-LAST:event_btnSalvarActionPerformed

